class Complex # :nodoc:
  alias x real
  alias y imag
end

DIRECTIONS = [1, 0 - 1i, -1, 0 + 1i].freeze
TURN = '&'
CROSSROADS = '+'
WALL = '#'
ROAD = '.'
START_POINT = 'S'
END_POINT = 'E'

def main(input)
  @floor = []
  @start_pos = nil
  @end_pos = nil
  input.lines.map(&:chomp).each_with_index do |line, _y|
    @start_pos ||= check(line, START_POINT)
    @end_pos ||= check(line, END_POINT)
    @floor << line
  end
  fill_dead_ends
  mark_turningpoints
  puts @floor
  find_neighbors(@start_pos, 1, 0).each do |dest, cost|
  graph = { @start_pos =>  }
  pp graph
  nodes = [@end_pos]
  each_square { nodes << _1 if get(_1) == ROAD }
  nodes.each do |node|
    graph[node] = {}
    ways_out(node).each { graph[node].merge!(find_neighbors(node, _1, 0)) }
  end
  result = dijkstra(graph, @start_pos)
  puts result[@end_pos]
end

def check(line, char)
  Complex(line.index(char), @floor.length) if line.index(char)
end

def fill_dead_ends
  loop do
    changed = false
    each_square do |pos|
      if get(pos) == ROAD && pos != @start_pos && pos != @end_pos && dead_end?(pos)
        set(pos, WALL)
        changed = true
      end
    end
    break unless changed
  end
end

def mark_turningpoints
  each_square do |pos|
    next if [@start_pos, @end_pos].include?(pos)

    ways_out = ways_out(pos)
    set(pos, CROSSROADS) if ways_out.length > 2
    set(pos, TURN) if ways_out.length == 2 && ways_out.sum.abs != 0
  end
end

def each_square
  @floor.each_with_index do |line, y|
    line.chars.each_with_index { |char, x| yield Complex(x, y) if char != WALL }
  end
end

def ways_out(pos) = DIRECTIONS.reject { get(pos + _1) == WALL }

def find_neighbors(pos, facing, cost)
  next_pos = pos + facing
  case get(next_pos)
  when ROAD, END_POINT
    { next_pos => cost + 1 }
  when START_POINT
    {} # Not useful to go back to start point.
  when TURN
    ways_out = ways_out(next_pos) - [-facing]
    find_neighbors(next_pos, ways_out.first, cost + 1001)
  when CROSSROADS
    ways_out = ways_out(next_pos) - [-facing]
    result = {}
    ways_out.each do |dir|
      score = (dir + facing).abs == 2 ? 0 : 1000
      result.merge!(find_neighbors(next_pos, dir, cost + score + 1))
    end
    result
  when WALL # Can only happen in @start_pos.
    result = {}
    ways_out(pos).each { result.merge!(find_neighbors(pos, _1, 1000)) }
    result
  end
end

def dead_end?(pos) = DIRECTIONS.count { get(pos + _1) == WALL } > 2

def get(pos) = @floor[pos.y][pos.x]

def set(pos, char)
  @floor[pos.y][pos.x] = char
end

def find_one_shortest_path(graph, now, target, path_info)
  if now == target
    print path_info
    return
  end

  graph[now].each do |neighbor_point|
    path_info << neighbor_point
    find_one_shortest_path(graph, neighbor_point, target, path_info) # recursion
    path_info = path_info[0...-1] # backtracking
  end
end

def count(path)
  @visited = {}
  puts 'loop'
  follow(path, @start_pos)
  puts 'done'
  p visited: @visited
end

def follow(path, pos)
  puts (' ' * caller.size) + "follow(path, #{pos})"
  @visited[pos] = true
  return unless pos != @end_pos

  path[pos]&.each do |next_pos|
    loop do
      way_out = ways_out(pos).find { |dir| (pos + dir - next_pos).abs < (pos - next_pos).abs }
      p pos: pos, next_pos: next_pos, way_out: way_out
      pos += way_out
      @visited[pos] = true
      break if pos == next_pos
    end
    follow(path, pos)
  end
end

